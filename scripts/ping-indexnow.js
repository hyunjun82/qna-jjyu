#!/usr/bin/env node
/**
 * ping-indexnow.js
 * Submits site URLs to IndexNow (Bing, Yandex, and other IndexNow partners).
 *
 * Usage:
 *   node scripts/ping-indexnow.js              # auto: reads out/sitemap.xml after build
 *   node scripts/ping-indexnow.js <url> ...    # manual: ping specific URLs
 *
 * Run after deploy:
 *   "postbuild": "node scripts/ping-indexnow.js"
 */

const https = require("https");
const fs = require("fs");
const path = require("path");

const KEY = "406811e3cf85a16c364b45702bd57245";
const HOST = "qna.jjyu.co.kr";
const BASE = `https://${HOST}`;
const KEY_LOCATION = `${BASE}/${KEY}.txt`;

/** Parse URLs from a sitemap.xml string */
function parseSitemap(xml) {
  const urls = [];
  const matches = xml.matchAll(/<loc>(.*?)<\/loc>/g);
  for (const m of matches) {
    urls.push(m[1].trim());
  }
  return urls;
}

/** Read out/sitemap.xml generated by next build (output: export) */
function readLocalSitemap() {
  const sitemapPath = path.join(__dirname, "..", "out", "sitemap.xml");
  if (!fs.existsSync(sitemapPath)) return null;
  return parseSitemap(fs.readFileSync(sitemapPath, "utf8"));
}

/** POST URL list to IndexNow */
function postIndexNow(urls) {
  return new Promise((resolve, reject) => {
    const body = JSON.stringify({
      host: HOST,
      key: KEY,
      keyLocation: KEY_LOCATION,
      urlList: urls,
    });

    const options = {
      hostname: "api.indexnow.org",
      path: "/IndexNow",
      method: "POST",
      headers: {
        "Content-Type": "application/json; charset=utf-8",
        "Content-Length": Buffer.byteLength(body, "utf8"),
      },
    };

    const req = https.request(options, (res) => {
      let data = "";
      res.on("data", (c) => (data += c));
      res.on("end", () => resolve({ status: res.statusCode, body: data }));
    });
    req.on("error", reject);
    req.write(body);
    req.end();
  });
}

async function main() {
  const args = process.argv.slice(2);
  let urls;

  if (args.length > 0) {
    urls = args;
  } else {
    urls = readLocalSitemap();
    if (!urls) {
      console.warn("out/sitemap.xml not found. Run 'next build' first, or pass URLs as arguments.");
      process.exit(0);
    }
    console.log(`Found ${urls.length} URLs in out/sitemap.xml`);
  }

  // IndexNow: max 10,000 URLs per request
  const BATCH = 10000;
  for (let i = 0; i < urls.length; i += BATCH) {
    const batch = urls.slice(i, i + BATCH);
    console.log(`Submitting ${batch.length} URLs to IndexNow...`);
    try {
      const result = await postIndexNow(batch);
      if (result.status === 200 || result.status === 202) {
        console.log(`✓ Accepted (HTTP ${result.status})`);
      } else {
        console.warn(`✗ HTTP ${result.status}: ${result.body}`);
      }
    } catch (err) {
      console.error("Error:", err.message);
    }
  }
}

main();
